namespace WBL\Theme;

# ------------------------------------------------------------------------------
# Hooking
# ------------------------------------------------------------------------------

add_action( 'after_setup_theme', function() {

    add_shortcode( 'year', __NAMESPACE__ . '\render_year' );

}, 5 );

# ------------------------------------------------------------------------------
# Functions
# ------------------------------------------------------------------------------

/**
 * Render year
 */
function render_year() {
    $year = date('Y');
    return $year;
}